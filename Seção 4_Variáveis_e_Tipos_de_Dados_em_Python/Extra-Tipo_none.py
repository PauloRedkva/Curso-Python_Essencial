"""
Tipo none
O tipo de dado none em Python representa o tipo sem tipo,
ou poderia ser conhecido também como tipo vazio.
É mais apropriado falar tipo sem tipo.

Obs: O tipo none é sempre especificado com a primeira letra maiuscula

Certo: None
Errado: none

Quando utilizamos?

Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo, antes de recebermos um valor final

Obs - O tipo None em python sempre é considerado falso
"""

numeros = None

print(numeros)
print(type(numeros))